package com.javaclass.basic.vo;

public class SizeVO {
	private int no;
	private String goodsSize;
	private int goodsSizeNo;
	private String sizeSearch;
	
	public SizeVO() {}
	
	public String getSizeSearch() {
		return sizeSearch;
	}

	public void setSizeSearch(String sizeSearch) {
		this.sizeSearch = sizeSearch;
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getGoodsSize() {
		return goodsSize;
	}

	public void setGoodsSize(String goodsSize) {
		this.goodsSize = goodsSize;
	}

	public int getGoodsSizeNo() {
		return goodsSizeNo;
	}

	public void setGoodsSizeNo(int goodsSizeNo) {
		this.goodsSizeNo = goodsSizeNo;
	}
	
	
	
}
